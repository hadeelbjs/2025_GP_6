class LawArticle {
  final String id;
  final String articleNumber; // رقم المادة — مثال: '٣'
  final String caseNumber; // رقم الحالة داخل المادة — مثال: '١'
  final String title; // عنوان مفهوم للمستخدم العادي
  final String simplifiedText; // شرح مبسط بالعربي العادي
  final String penalty; // العقوبة — نفسها لكل حالات نفس المادة
  final String example; // مثال توضيحي من الحياة اليومية
  final List<String> keywords; // كلمات للبحث

  const LawArticle({
    required this.id,
    required this.articleNumber,
    required this.caseNumber,
    required this.title,
    required this.simplifiedText,
    required this.penalty,
    required this.example,
    required this.keywords,
  });
}

